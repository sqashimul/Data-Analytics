
#Python Comparison Operators
a = (5<9) #Less Than '<'
b = (3<=7) #Less than or Equal to '<='
c = (5!=4) #Not Equal To '!='
d = (5==5) #Equal to '=='
e = (9>=7) #Greater Than or Equal to '>='
f = (9>8) # Greater Than '>'
print("True Statement")
print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
