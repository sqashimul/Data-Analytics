#Type casting
Name = "Jhon"
age = 25
height = 5.8
print(type(age))
print(type(Name))
print(type(height))

# Implicit Type
a = 15.5
b = 5
c = a+b
print(c)
print(type(c))

# Explicit Type
x = "15.5"
y = 5
x = float(x)
print("After Conversion:", type(x))
z = x+y
print(z)
print(type(z))

